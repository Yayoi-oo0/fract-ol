#include "fractol.h"

int	get_color_from_iter(int iter)
{
	int	r;
	int	g;
	int	b;

	r = (int)((iter % 256) * (256 - (iter % 256)) / 65);
	g = (int)(((iter + 85) % 256) * (256 - ((iter + 85) % 256)) / 65);
	b = (int)(((iter + 170) % 256) * (256 - ((iter + 170) % 256)) / 65);
	return ((r << 16) | (g << 8) | b);
}
